package com.autoscholardb.demo.model.Author;

public class AuthorInterest {
    private String title;
    private String link;
    private String serpapi_link;

    // Getters and Setters
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getSerpapi_link() {
        return serpapi_link;
    }

    public void setSerpapi_link(String serpapi_link) {
        this.serpapi_link = serpapi_link;
    }
}
